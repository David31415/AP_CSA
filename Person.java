public class Person{
    double height; // The Person must have a height
    int age; // age
    String name; // name
    String hairColor; // color of hair

    Person(double h, int how_old, String n, String hC){ // Defining a Constructor
        height = h; // Making the height input equal to the input in the Constructor.
        age = how_old; // Setting the input age equal to constructor.
        name = n; // name input.
        hairColor = hC; // haircolor input.
    }

}